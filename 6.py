from turtle import *

tracer(0)

m = 20
rt(315)
for i in range(7):
    fd(16 * m)  #прямо(fd)
    rt(45)      #вправо(rt)
    fd(8 * m)
    rt(135)
up()

for x in range(-20, 20):
    for y in range(-20, 20):
        goto(x * m, y * m)
        dot(5, 'blue')
update()
input()
